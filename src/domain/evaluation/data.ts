export type DataKind = (typeof dataKinds)[number][0]

const dataKindSet = new Set<string>(dataKinds.map(([kind]) => kind))
const writable = new Set<string>(dataKinds.filter(([, value]) => value).map(([kind]) => kind))
const schema = evaluationSchema as {
  readonly $schema: string
  readonly $defs: Readonly<Record<string, Readonly<Record<string, unknown>>>>
}

export const resolveDataKind = (value: string): DataKind => {
  if (!dataKindSet.has(value))
    throw new Error(`unknown evaluation data kind ${JSON.stringify(value)}`)
  return value as DataKind
}

export const evaluationDataSchema = (kind?: DataKind) => {
  if (kind === undefined) return schema
  return {
    $id: `https://getquality.md/evaluation-data.schema.json/${kind}`,
    $schema: schema.$schema,
    ...schema.$defs[kind],
  }
}

export const evaluationDataExample = (kind: DataKind) =>
  (evaluationExamples as Readonly<Record<DataKind, Record<string, unknown>>>)[kind]

const ajvError = (errors: ReadonlyArray<ErrorObject> | null | undefined) =>
  (errors ?? [])
    .map((error) => `${error.instancePath || "payload"} ${error.message ?? "is invalid"}`)
    .join("; ")

export const validateDataPayload = (
  payload: Record<string, unknown>,
  model?: QualityModel,
  allowCliOwned = false,
) => {
  const kind = resolveDataKind(typeof payload.kind === "string" ? payload.kind : "")
  if (!allowCliOwned && !writable.has(kind)) throw new Error(`${kind} is CLI-owned`)
  const ajv = new Ajv2020({ allErrors: true, strict: false, validateFormats: false })
  const check = ajv.compile(evaluationDataSchema(kind))
  if (!check(payload)) throw new Error(ajvError(check.errors))
  if (model !== undefined) validateModelReferences(payload, model)
  validatePayloadSemantics(kind, payload)
  return kind
}

const validatePayloadSemantics = (kind: DataKind, payload: Record<string, unknown>) => {
  if (kind === "RequirementAssessmentResult" && Array.isArray(payload.findings)) {
    for (const [index, raw] of payload.findings.entries()) {
      const finding = raw as Record<string, unknown>
      const type = String(finding.type ?? "")
      const hasSeverity = Object.hasOwn(finding, "severity")
      if (["gap", "risk"].includes(type) && !hasSeverity)
        throw new Error(
          `RequirementAssessmentResult.findings[${index}] is missing required field severity for ${type} finding`,
        )
      if (["strength", "note"].includes(type) && hasSeverity)
        throw new Error(
          `RequirementAssessmentResult.findings[${index}].severity is forbidden when type is ${type}`,
        )
      if (Array.isArray(finding.candidateActions)) {
        const actionIds = finding.candidateActions.map((item) =>
          String((item as Record<string, unknown>).id ?? ""),
        )
        for (const [actionIndex, id] of actionIds.entries()) {
          if (actionIds.indexOf(id) !== actionIndex)
            throw new Error(
              `RequirementAssessmentResult.findings[${index}].candidateActions[${actionIndex}].id ${JSON.stringify(id)} is duplicated within RequirementAssessmentResult.findings[${index}].candidateActions`,
            )
        }
      }
    }
  }
  if (kind === "RecommendationResult") {
    for (const field of [
      "effort",
      "roi",
      "ROI",
      "quickWin",
      "quick-win",
      "backlogPriority",
      "priority",
      "score",
    ])
      if (Object.hasOwn(payload, field))
        throw new Error(`RecommendationResult contains forbidden planning field ${field}`)
  }
}

const validateModelReferences = (payload: Record<string, unknown>, model: QualityModel) => {
  const references = new Set([
    ...flattenModel(projectModel(model)).map((element) => element.id),
    ...model.ratingScale.map((level) => `rating:${level.level}`),
  ])
  const visit = (value: unknown, key = "") => {
    if (typeof value === "string") {
      if (
        (key.endsWith("Id") || key.endsWith("Ref")) &&
        /^(?:area|factor|requirement|rating):/.test(value) &&
        !references.has(value)
      )
        throw new Error(`${key} ${JSON.stringify(value)} does not resolve in the model`)
      return
    }
    if (Array.isArray(value)) {
      for (const entry of value) visit(entry, key.endsWith("Ids") ? key.slice(0, -1) : key)
      return
    }
    if (value !== null && typeof value === "object")
      for (const [childKey, child] of Object.entries(value)) visit(child, childKey)
  }
  visit(payload)
}

const subject = (payload: Record<string, unknown>) =>
  payload.subject !== null && typeof payload.subject === "object"
    ? (payload.subject as Record<string, unknown>)
    : {}

const refParts = (value: unknown, prefix: string) => {
  if (typeof value !== "string" || !value.startsWith(prefix))
    throw new Error(`payload must carry ${prefix.slice(0, -1)} reference`)
  const raw = value.slice(prefix.length)
  return raw === "root" ? [] : raw.split("/")
}

const areaBase = (parts: ReadonlyArray<string>) =>
  `data/areas/${parts.length === 0 ? "root" : parts.join("/")}`

const areaPath = (reference: unknown, file: string) =>
  `${areaBase(refParts(reference, "area:"))}/${file}`

const requirementPath = (reference: unknown, file: string) => {
  if (typeof reference !== "string" || !reference.startsWith("requirement:"))
    throw new Error("payload must carry requirementId")
  const [area = "", name = ""] = reference.slice(12).split("::")
  return `${areaBase(area === "root" ? [] : area.split("/"))}/requirements/${name}/${file}`
}

const factorPath = (reference: unknown, file: string) => {
  if (typeof reference !== "string" || !reference.startsWith("factor:"))
    throw new Error("payload must carry factorId")
  const [area = "", factor = ""] = reference.slice(7).split("::")
  const parts = factor.split("/").flatMap((name) => ["factors", name])
  return `${areaBase(area === "root" ? [] : area.split("/"))}/${parts.join("/")}/${file}`
}

export const dataPathForPayload = (kind: DataKind, payload: Record<string, unknown>) => {
  const payloadSubject = subject(payload)
  switch (kind) {
    case "EvaluationManifest":
      return "data/evaluation-manifest.json"
    case "EvaluationFrame":
      return "data/frame/evaluation-frame.json"
    case "AreaEvaluationFrame":
      return areaPath(payloadSubject.areaId, "area-evaluation-frame.json")
    case "AreaAnalysisFrame":
      return areaPath(payloadSubject.areaId, "area-analysis-frame.json")
    case "AreaAnalysisResult":
      return areaPath(payload.areaId, "area-analysis-result.json")
    case "RequirementEvaluationFrame":
      return requirementPath(payloadSubject.requirementId, "requirement-evaluation-frame.json")
    case "RequirementAssessmentResult":
      return requirementPath(payload.requirementId, "requirement-assessment-result.json")
    case "RequirementRatingResult":
      return requirementPath(payload.requirementId, "requirement-rating-result.json")
    case "FactorAnalysisFrame":
      return factorPath(payloadSubject.factorId, "factor-analysis-frame.json")
    case "FactorAnalysisResult":
      return factorPath(payload.factorId, "factor-analysis-result.json")
    case "FindingRankingResult":
      return "data/advice/finding-ranking-result.json"
    case "RecommendationResult": {
      const id = payload.id
      if (typeof id !== "string" || !/^qrec_[a-z0-9]+$/.test(id))
        throw new Error("RecommendationResult.id must match qrec_<token>")
      return `data/advice/recommendations/${id}/recommendation-result.json`
    }
    case "RecommendationRankingResult":
      return "data/advice/recommendation-ranking-result.json"
    case "EvaluationSummaryResult":
      return "data/advice/evaluation-summary-result.json"
    case "EvaluationOutputResult":
      return "data/evaluation-output-result.json"
  }
}

export interface DataQuery {
  readonly kind: DataKind
  readonly area: string
  readonly factor: string
  readonly requirement: string
  readonly selector: string
}

export const dataPathForQuery = (query: DataQuery) => {
  switch (query.kind) {
    case "EvaluationManifest":
      return "data/evaluation-manifest.json"
    case "EvaluationFrame":
      return "data/frame/evaluation-frame.json"
    case "AreaEvaluationFrame":
      return areaPath(query.area, "area-evaluation-frame.json")
    case "AreaAnalysisFrame":
      return areaPath(query.area, "area-analysis-frame.json")
    case "AreaAnalysisResult":
      return areaPath(query.area, "area-analysis-result.json")
    case "RequirementEvaluationFrame":
      return requirementPath(query.requirement, "requirement-evaluation-frame.json")
    case "RequirementAssessmentResult":
      return requirementPath(query.requirement, "requirement-assessment-result.json")
    case "RequirementRatingResult":
      return requirementPath(query.requirement, "requirement-rating-result.json")
    case "FactorAnalysisFrame":
      return factorPath(query.factor, "factor-analysis-frame.json")
    case "FactorAnalysisResult":
      return factorPath(query.factor, "factor-analysis-result.json")
    case "FindingRankingResult":
      return "data/advice/finding-ranking-result.json"
    case "RecommendationResult":
      if (!/^qrec_[a-z0-9]+$/.test(query.selector))
        throw new Error("--selector must name the RecommendationResult id")
      return `data/advice/recommendations/${query.selector}/recommendation-result.json`
    case "RecommendationRankingResult":
      return "data/advice/recommendation-ranking-result.json"
    case "EvaluationSummaryResult":
      return "data/advice/evaluation-summary-result.json"
    case "EvaluationOutputResult":
      return "data/evaluation-output-result.json"
  }
}

export const assignRecommendationId = (payload: Record<string, unknown>, token: string) => {
  if (payload.kind !== "RecommendationResult" || /^qrec_[a-z0-9]+$/.test(String(payload.id ?? "")))
    return payload
  return { ...payload, id: `qrec_${token}` }
}
