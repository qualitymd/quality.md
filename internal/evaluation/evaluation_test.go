func TestSetDataRejectsInvalidV2Shapes(t *testing.T) {
	repo := testRepo(t)
	result, err := CreateRun(Options{RepoRoot: repo, Model: "QUALITY.md"})
	if err != nil {
		t.Fatalf("CreateRun() error = %v", err)
	}
	runPath := filepath.Join(repo, result.Path)
	cases := []string{
		`{"schemaVersion":1,"kind":"AreaAnalysisResult","areaId":[],"localAnalysis":{"status":"rated","ratingDrivers":[]},"localAndDescendantAnalysis":{"status":"analyzed","ratingDrivers":[]}}`,
		`{"schemaVersion":1,"kind":"FactorAnalysisResult","factorId":{"declaringAreaId":[],"factorPath":["reliability"]},"localAnalysis":{"status":"analyzed","ratingDrivers":["finding-1"]},"localAndDescendantAnalysis":{"status":"analyzed","ratingDrivers":[]}}`,
		`{"schemaVersion":1,"kind":"RequirementAssessmentResult","requirementId":{"declaringAreaId":[],"requirementName":"has-tests"},"status":"assessed","findings":["finding-1"]}`,
	}
	for _, raw := range cases {
		if _, err := SetData(runPath, []byte(raw), DataSetOptions{}); err == nil {
			t.Fatalf("SetData(%s) error = nil, want validation error", raw)
		}
	}
}

func TestRequirementTitleFindsFactorDeclaredRequirement(t *testing.T) {
	repo := testRepoWithModel(t, `---
title: Test model
ratingScale:
  - level: target
    title: Target
    description: Target.
    criterion: Meets it.
factors:
  reliability:
    title: Reliability
    requirements:
      retry-window:
        title: Retry window is bounded
        assessment: Inspect retry behavior.
---
`)
	result, err := CreateRun(Options{RepoRoot: repo, Model: "QUALITY.md"})
	if err != nil {
		t.Fatalf("CreateRun() error = %v", err)
	}
	run, err := Inspect(filepath.Join(repo, result.Path))
	if err != nil {
		t.Fatalf("Inspect() error = %v", err)
	}
	got := requirementTitle(run.Model, requirementID{Name: "retry-window"})
	if got != "Retry window is bounded" {
		t.Fatalf("requirementTitle() = %q, want Factor-declared Requirement title", got)
	}
}

